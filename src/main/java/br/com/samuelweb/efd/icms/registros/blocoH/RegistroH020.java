public class RegistroH020 {
	
private final String reg = "H020";
	private String cst_icms;
	private String bc_icms;
	private String vl_icms;
	/**
	 * @return the cst_icms
	 */
	public String getCst_icms() {
		return cst_icms;
	}
	/**
	 * @param cst_icms the cst_icms to set
	 */
	public void setCst_icms(String cst_icms) {
		this.cst_icms = cst_icms;
	}
	/**
	 * @return the bc_icms
	 */
	public String getBc_icms() {
		return bc_icms;
	}
	/**
	 * @param bc_icms the bc_icms to set
	 */
	public void setBc_icms(String bc_icms) {
		this.bc_icms = bc_icms;
	}
	/**
	 * @return the vl_icms
	 */
	public String getVl_icms() {
		return vl_icms;
	}
	/**
	 * @param vl_icms the vl_icms to set
	 */
	public void setVl_icms(String vl_icms) {
		this.vl_icms = vl_icms;
	}
	/**
	 * @return the reg
	 */
	public String getReg() {
		return reg;
	}
	
}
