package br.com.samuelweb.efd.icms.registros.blocoC;

/**
 * @author Samuel Oliveira
 *
 */
public class RegistroC370 {

	private final String reg = "C370";
	private String num_item;
	private String cod_item;
	private String qtd;
	private String unid;
	private String vl_item;
	private String vl_desc;

	/**
	 * @return the num_item
	 */
	public String getNum_item() {
		return num_item;
	}

	/**
	 * @param num_item
	 *            the num_item to set
	 */
	public void setNum_item(String num_item) {
		this.num_item = num_item;
	}

	/**
	 * @return the cod_item
	 */
	public String getCod_item() {
		return cod_item;
	}

	/**
	 * @param cod_item
	 *            the cod_item to set
	 */
	public void setCod_item(String cod_item) {
		this.cod_item = cod_item;
	}

	/**
	 * @return the qtd
	 */
	public String getQtd() {
		return qtd;
	}

	/**
	 * @param qtd
	 *            the qtd to set
	 */
	public void setQtd(String qtd) {
		this.qtd = qtd;
	}

	/**
	 * @return the unid
	 */
	public String getUnid() {
		return unid;
	}

	/**
	 * @param unid
	 *            the unid to set
	 */
	public void setUnid(String unid) {
		this.unid = unid;
	}

	/**
	 * @return the vl_item
	 */
	public String getVl_item() {
		return vl_item;
	}

	/**
	 * @param vl_item
	 *            the vl_item to set
	 */
	public void setVl_item(String vl_item) {
		this.vl_item = vl_item;
	}

	/**
	 * @return the vl_desc
	 */
	public String getVl_desc() {
		return vl_desc;
	}

	/**
	 * @param vl_desc
	 *            the vl_desc to set
	 */
	public void setVl_desc(String vl_desc) {
		this.vl_desc = vl_desc;
	}

	/**
	 * @return the reg
	 */
	public String getReg() {
		return reg;
	}

}
