/**
 * @author Samuel Oliveira
 */
public class RegistroK235 {

    private final String reg = "K235";
    private String dt_saida;
    private String cod_item;
    private String qtd;
    private String cod_ins_subst;

    /**
     * @return the dt_saida
     */
    public String getDt_saida() {
        return dt_saida;
    }

    /**
     * @param dt_saida the dt_saida to set
     */
    public void setDt_saida(String dt_saida) {
        this.dt_saida = dt_saida;
    }

    /**
     * @return the cod_item
     */
    public String getCod_item() {
        return cod_item;
    }

    /**
     * @param cod_item the cod_item to set
     */
    public void setCod_item(String cod_item) {
        this.cod_item = cod_item;
    }

    /**
     * @return the qtd
     */
    public String getQtd() {
        return qtd;
    }

    /**
     * @param qtd the qtd to set
     */
    public void setQtd(String qtd) {
        this.qtd = qtd;
    }

    /**
     * @return the cod_ins_subst
     */
    public String getCod_ins_subst() {
        return cod_ins_subst;
    }

    /**
     * @param cod_ins_subst the cod_ins_subst to set
     */
    public void setCod_ins_subst(String cod_ins_subst) {
        this.cod_ins_subst = cod_ins_subst;
    }

    /**
     * @return the reg
     */
    public String getReg() {
        return reg;
    }

}
