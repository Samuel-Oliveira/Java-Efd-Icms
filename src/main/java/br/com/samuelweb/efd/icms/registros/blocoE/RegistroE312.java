package br.com.samuelweb.efd.icms.registros.blocoE;

/**
 * @author Samuel Oliveira
 */
public class RegistroE312 {

	private final String reg = "E312";
	private String num_da;
	private String num_proc;
	private String ind_proc;
	private String proc;
	private String txt_compl;

	/**
	 * @return the num_da
	 */
	public String getNum_da() {
		return num_da;
	}

	/**
	 * @param num_da
	 *            the num_da to set
	 */
	public void setNum_da(String num_da) {
		this.num_da = num_da;
	}

	/**
	 * @return the num_proc
	 */
	public String getNum_proc() {
		return num_proc;
	}

	/**
	 * @param num_proc
	 *            the num_proc to set
	 */
	public void setNum_proc(String num_proc) {
		this.num_proc = num_proc;
	}

	/**
	 * @return the ind_proc
	 */
	public String getInd_proc() {
		return ind_proc;
	}

	/**
	 * @param ind_proc
	 *            the ind_proc to set
	 */
	public void setInd_proc(String ind_proc) {
		this.ind_proc = ind_proc;
	}

	/**
	 * @return the proc
	 */
	public String getProc() {
		return proc;
	}

	/**
	 * @param proc
	 *            the proc to set
	 */
	public void setProc(String proc) {
		this.proc = proc;
	}

	/**
	 * @return the txt_compl
	 */
	public String getTxt_compl() {
		return txt_compl;
	}

	/**
	 * @param txt_compl
	 *            the txt_compl to set
	 */
	public void setTxt_compl(String txt_compl) {
		this.txt_compl = txt_compl;
	}

	/**
	 * @return the reg
	 */
	public String getReg() {
		return reg;
	}

}
