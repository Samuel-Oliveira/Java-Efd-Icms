package br.com.swconsultoria.efd.icms.registros.blocoB;

/**
 * @author Sidnei Klein
 */
public class RegistroB510 {

    private final String reg = "B510";
    private String ind_prof;
    private String ind_esc;
    private String ind_soc;
    private String cpf;
    private String nome;

    /**
     * @return the reg
     */
    public String getReg() {
        return reg;
    }

    /**
     * @return the ind_prof
     */
    public String getInd_prof() {
        return ind_prof;
    }

    /**
     * @param ind_prof the ind_prof to set
     */
    public void setInd_prof(String ind_prof) {
        this.ind_prof = ind_prof;
    }

    /**
     * @return the ind_esc
     */
    public String getInd_esc() {
        return ind_esc;
    }

    /**
     * @param ind_esc the ind_esc to set
     */
    public void setInd_esc(String ind_esc) {
        this.ind_esc = ind_esc;
    }

    /**
     * @return the ind_soc
     */
    public String getInd_soc() {
        return ind_soc;
    }

    /**
     * @param ind_soc the ind_soc to set
     */
    public void setInd_soc(String ind_soc) {
        this.ind_soc = ind_soc;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

}
